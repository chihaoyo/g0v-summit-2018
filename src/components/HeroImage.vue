<script>
import { TweenLite as tween, Power1, Power3 } from 'gsap'
import HeroImageMobile from '@/components/HeroImageMobile'
import { mapState } from 'vuex'

export default {
  name: 'HeroImage',
  components: { HeroImageMobile },
  data() {
    return {
      vh: window.innerHeight,
      vw: window.innerWidth,
      pdx: 1,
      pdy: 1,
    }
  },
  // svg 內無法用 <TW/>、<EN/>
  computed: mapState(['lang']),
  mounted() {
    window.addEventListener('resize', this.handleResize)
    this.tweenShootingStars()
    this.tweenFadeInItems()
  },
  destroied() {
    window.removeEventListener('resize', this.handleResize)
  },
  methods: {
    tweenShootingStars() {
      Object.keys(this.$refs)
        .filter(key => key.includes('shooting-star-'))
        .forEach(key => {
          const node = this.$refs[key]
          const { duration, delay } = node.dataset

          tween.from(node, duration || 1, {
            x: `+=${this.vw}`,
            delay: delay || 0.3,
            ease: Power3.SlowMo,
          })
        })
    },
    tweenFadeInItems() {
      Object.keys(this.$refs)
        .filter(key => key.includes('fade-in-'))
        .forEach(key => {
          const node = this.$refs[key]
          const { duration, delay } = node.dataset

          tween.from(node, duration || 0.5, {
            opacity: 0,
            delay: delay || 0,
            ease: Power1.SlowMo,
          })
        })
    },
    handleMousemove(event) {
      const moveX = event.clientX
      const moveY = event.clientY
      const dx = moveX - this.vw / 2
      const dy = moveY - this.vh / 2
      this.pdx = Math.sign(dx) * Math.sqrt(Math.abs(dx))
      this.pdy = Math.sign(dy) * Math.sqrt(Math.abs(dy))
    },
    handleResize(event) {
      this.vh = window.innerHeight
      this.vw = window.innerWidth
    },
  },
}
